// BatchDiscoverKeyword discovers relationships using FTS5 keyword matching instead of AI.
// For each memory, runs an FTS5 search and creates 'similar' relationships where
// BM25 score exceeds threshold. Much faster than Ollama-based discover.
func (s *Service) BatchDiscoverKeyword(limit int, minScore float64, domain string) (int, error) {
	if limit <= 0 {
		limit = 100
	}
	if minScore <= 0 {
		minScore = 0.3
	}

	// Get memories to process
	filters := &database.MemoryFilters{
		Limit: limit,
	}
	if domain != "" {
		filters.Domain = domain
	}

	memories, err := s.db.ListMemories(filters)
	if err != nil {
		return 0, fmt.Errorf("failed to list memories: %w", err)
	}

	created := 0
	for _, mem := range memories {
		// Extract keywords from content (first 100 chars)
		keywords := extractKeywords(mem.Content, 5)
		if len(keywords) == 0 {
			continue
		}

		// Search for similar memories using FTS5
		searchResults, err := s.db.SearchFTS(strings.Join(keywords, " OR "), &database.SearchFilters{
			Limit:  5,
			Domain: domain,
		})
		if err != nil {
			continue
		}

		for _, result := range searchResults {
			if result.Memory.ID == mem.ID {
				continue
			}
			if result.Relevance < minScore {
				continue
			}

			// Check if relationship already exists
			existing, err := s.db.GetRelationshipsBetween(mem.ID, result.Memory.ID)
			if err != nil || len(existing) > 0 {
				continue
			}

			// Create similar relationship
			rel := &database.Relationship{
				SourceMemoryID:   mem.ID,
				TargetMemoryID:   result.Memory.ID,
				RelationshipType: "similar",
				Strength:         result.Relevance,
				Context:          "Auto-discovered via keyword similarity",
				AutoGenerated:    true,
			}
			if err := s.db.CreateRelationship(rel); err == nil {
				created++
			}
		}
	}

	return created, nil
}

// DomainCluster creates 'similar' relationships between memories in the same
// domain that share 2+ tags. Creates dense clusters within domains.
func (s *Service) DomainCluster(domain string, minSharedTags int, limit int) (int, error) {
	if minSharedTags <= 0 {
		minSharedTags = 2
	}
	if limit <= 0 {
		limit = 100
	}

	pairs, err := s.db.GetMemoriesByDomainAndTags(domain, nil, minSharedTags, limit)
	if err != nil {
		return 0, fmt.Errorf("failed to get domain pairs: %w", err)
	}

	created := 0
	for _, pair := range pairs {
		// Check if relationship already exists
		existing, err := s.db.GetRelationshipsBetween(pair[0], pair[1])
		if err != nil || len(existing) > 0 {
			continue
		}

		rel := &database.Relationship{
			SourceMemoryID:   pair[0],
			TargetMemoryID:   pair[1],
			RelationshipType: "similar",
			Strength:         0.6,
			Context:          "Auto-discovered via domain tag clustering",
			AutoGenerated:    true,
		}
		if err := s.db.CreateRelationship(rel); err == nil {
			created++
		}
	}

	return created, nil
}

// extractKeywords extracts the most significant words from content
func extractKeywords(content string, maxKeywords int) []string {
	// Remove common stop words and short words
	stopWords := map[string]bool{
		"the": true, "a": true, "an": true, "and": true, "or": true,
		"but": true, "in": true, "on": true, "at": true, "to": true,
		"for": true, "of": true, "with": true, "by": true, "is": true,
		"it": true, "this": true, "that": true, "was": true, "are": true,
		"be": true, "has": true, "had": true, "have": true, "from": true,
		"not": true, "will": true, "can": true, "as": true, "we": true,
		"all": true, "been": true, "would": true, "there": true, "their": true,
	}

	words := strings.Fields(strings.ToLower(content))
	wordCount := make(map[string]int)

	for _, w := range words {
		// Clean punctuation
		w = strings.Trim(w, ".,;:!?\"'()[]{}#*-_/\\")
		if len(w) < 3 || stopWords[w] {
			continue
		}
		wordCount[w]++
	}

	// Get top keywords by frequency
	var keywords []string
	for i := 0; i < maxKeywords; i++ {
		maxWord := ""
		maxCount := 0
		for w, c := range wordCount {
			if c > maxCount {
				maxWord = w
				maxCount = c
			}
		}
		if maxWord == "" {
			break
		}
		keywords = append(keywords, maxWord)
		delete(wordCount, maxWord)
	}

	return keywords
}

