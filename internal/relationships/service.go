// DecayRelationships reduces strength of relationships that haven't been
// accessed recently, and strengthens frequently-used ones.
func (s *Service) DecayRelationships(maxAge int, decayFactor float64) (int, error) {
	if maxAge <= 0 {
		maxAge = 30 // days
	}
	if decayFactor <= 0 || decayFactor >= 1 {
		decayFactor = 0.95
	}

	// Decay relationships not accessed in maxAge days
	result, err := s.db.Exec(
		`UPDATE memory_relationships
		SET strength = MAX(0.05, strength * ?)
		WHERE last_accessed_at IS NOT NULL
		  AND last_accessed_at < datetime('now', '-' || ? || ' days')
		  AND strength > 0.05`,
		decayFactor, maxAge)
	if err != nil {
		return 0, fmt.Errorf("failed to decay relationships: %w", err)
	}

	rows, _ := result.RowsAffected()
	return int(rows), nil
}

// TouchRelationship marks a relationship as accessed, strengthening it
func (s *Service) TouchRelationship(relationshipID string) error {
	_, err := s.db.Exec(
		`UPDATE memory_relationships
		SET last_accessed_at = datetime('now'),
		    access_count = COALESCE(access_count, 0) + 1,
		    strength = MIN(1.0, strength * 1.05)
		WHERE id = ?`, relationshipID)
	return err
}

// FindBridgeNodes finds memories that connect otherwise disconnected domains
func (s *Service) FindBridgeNodes(limit int) ([]BridgeNode, error) {
	if limit <= 0 {
		limit = 10
	}

	// Find memories that have relationships to memories in different domains
	rows, err := s.db.Query(`
		SELECT m.id, m.domain, COUNT(DISTINCT other_domain) as bridge_count
		FROM memories m
		JOIN (
			SELECT
				r.source_memory_id as mem_id,
				m2.domain as other_domain
			FROM memory_relationships r
			JOIN memories m2 ON r.target_memory_id = m2.id
			WHERE m2.domain IS NOT NULL AND m2.domain != ''
			UNION ALL
			SELECT
				r.target_memory_id as mem_id,
				m2.domain as other_domain
			FROM memory_relationships r
			JOIN memories m2 ON r.source_memory_id = m2.id
			WHERE m2.domain IS NOT NULL AND m2.domain != ''
		) connected ON connected.mem_id = m.id AND connected.other_domain != COALESCE(m.domain, '')
		WHERE m.domain IS NOT NULL AND m.domain != ''
		GROUP BY m.id
		HAVING bridge_count >= 2
		ORDER BY bridge_count DESC
		LIMIT ?
	`, limit)
	if err != nil {
		return nil, fmt.Errorf("failed to find bridge nodes: %w", err)
	}
	defer rows.Close()

	var bridges []BridgeNode
	for rows.Next() {
		var b BridgeNode
		if err := rows.Scan(&b.MemoryID, &b.Domain, &b.ConnectedDomains); err != nil {
			continue
		}
		bridges = append(bridges, b)
	}

	return bridges, nil
}

// BridgeNode represents a memory that bridges multiple domains
type BridgeNode struct {
	MemoryID         string `json:"memory_id"`
	Domain           string `json:"domain"`
	ConnectedDomains int    `json:"connected_domains"`
}

